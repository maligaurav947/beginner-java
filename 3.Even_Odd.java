// Check The Number is Even or Odd

import java.lang.*;

class Even_Odd {
 public static void main(String[] args) {
  int n = 3;
  if (n % 2 == 0) {
   System.out.println("Even");
  } else if (n % 2 != 0) {
   System.out.println("ODD");
  } else {
   System.out.println("Check The Number");
  }
 }
}